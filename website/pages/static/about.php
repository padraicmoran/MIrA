
<h1 class="mt-5 mb-4">About MIrA</h1>

<p>This resource aims to provide useful information for researchers on early Irish manuscript culture before c. AD 1000. 
	('Irish' here is shorthand for the broader Gaelic world, including early medieval Scotland and Man.) 
</p>

<p>The data was compiled by <a target="_blank" href="https://www.pmoran.ie/">Pádraic Moran</a> (Classics, University of Galway), based initially on the 
following key reference works: </p>

<ul>
<li>Bernhard Bischoff, <i>Südostdeutschen Schreibschulen und Bibliotheken in der Karolingerzeit</i>, 2 vols (Wiesbaden, 1940–1980).</li>
<li>Bernhard Bischoff, <i>Katalog der festländischen Handschriften des neunten Jahrhunderts</i>, 3 vols (Wiesbaden, 1998–2014).</li>
<li>E. A. Lowe, <i>Codices Latini Antiquiores</i>, 12 vols (Oxford, 1934–1971), digitised and updated at Mark Stansbury, <a target="_blank" href="https://elmss.nuigalway.ie/">Early Latin Manuscripts</a>.</li>
</ul>

<p>Pádraic Moran was also responsible for building the website interface and functionality.</p> 

<p>The resource remains a work in progress.
	Additions, corrections, suggestions welcome: <a href="mailto:padraic.moran@universityofgalway.ie">padraic.moran@universityofgalway.ie</a>.</p>


